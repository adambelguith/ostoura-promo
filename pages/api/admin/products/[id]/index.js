import { getSession } from 'next-auth/react';
import {Product} from '../../../../../models/Product';
import db from '../../../../../utils/db';
import cloudinary from '../../cloudinary_config'

const handler = async (req, res) => {
  const session = await getSession({ req });
  if (!session || (session && !session.user.isAdmin)) {
    return res.status(401).send('signin required');
  }

  const { user } = session;
  if (req.method === 'GET') {
    return getHandler(req, res, user);
  } else if (req.method === 'PUT') {
    return putHandler(req, res, user);
  } else if (req.method === 'DELETE') {
    return deleteHandler(req, res, user);
  } else {
    return res.status(400).send({ message: 'Method not allowed' });
  }
};
const getHandler = async (req, res) => {
  await db.connect();
  const product = await Product.findById(req.query.id).populate('category').populate('subcategory').exec();
  await db.disconnect();
  res.send(product);
};
const putHandler = async (req, res) => {
  await db.connect();
  const product = await Product.findById(req.query.id);
  if (product) {
    
    product.name = req.body.name;
    product.slug = req.body.slug;
    product.price = req.body.price;
    product.category = req.body.category;
    product.subcategory = req.body.subcategory;
    product.promotion = req.body.promotion ? req.body.promotion : null
    product.image = req.body.image;
    product.video = req.body.video;
    product.brand = req.body.brand;
    product.countInStock = req.body.countInStock;
    product.description = req.body.description;
    await product.save();
    await db.disconnect();
    res.send({ message: 'Product updated successfully' });
  } else {
    await db.disconnect();
    res.status(404).send({ message: 'Product not found' });
  }
};
const deleteHandler = async (req, res) => {
  await db.connect();
  const product = await Product.findById(req.query.id);
  if (product) {
    const listImages = product.image
    listImages.map(async (image) => {
      const imageSplit = image.split('/').pop().split('.')[0];
      await cloudinary.uploader.destroy(imageSplit);
    })
    if (product.video){
      const videoSplit = product.video.split('/').pop().split('.')[0];
      await cloudinary.uploader.destroy(videoSplit);
    }
    await product.remove();
    await db.disconnect();
    res.send({ message: 'Product deleted successfully' });
  } else {
    await db.disconnect();
    res.status(404).send({ message: 'Product not found' });
  }
};
export default handler;
